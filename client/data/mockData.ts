// Função auxiliar para gerar datas do mês atual
const getCurrentMonthDate = (day: number): string => {
  const now = new Date();
  const year = now.getFullYear();
  const month = now.getMonth(); // 0-11
  return `${year}-${String(month + 1).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
};

// Função auxiliar para gerar datas do mês anterior
const getPreviousMonthDate = (day: number): string => {
  const now = new Date();
  const year = now.getFullYear();
  const month = now.getMonth(); // 0-11
  const prevMonth = month === 0 ? 11 : month - 1;
  const prevYear = month === 0 ? year - 1 : year;
  return `${prevYear}-${String(prevMonth + 1).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
};

